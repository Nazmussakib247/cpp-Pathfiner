# include <iostream>
using namespace std;

int main() {
    //Declaring variable
    int height = 12;
    int width = 10;

    // Calculating area
    int area = 0.5 * height * width;

    // Printing values
    cout << "Height is: " << height << endl;
    cout << "Width is: " << width << endl;
    cout << "Area is: " << area;

    return 0;
}